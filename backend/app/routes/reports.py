def _ensure_demo_recommendations(user_id):
    """Auto-run analysis in demo mode if no recommendations exist."""
    if not is_demo_mode():
        return
    count = query_db(
        "SELECT COUNT(*) AS cnt FROM recommendations WHERE user_id = ? AND status = 'ACTIVE'",
        (user_id,), one=True
    )
    if count and count['cnt'] == 0:
        logger.info('Demo mode: auto-running analysis for report (user %s)', user_id)
        try:
            from .analysis import _run_analysis_for_user
            _run_analysis_for_user(user_id)
        except Exception as e:
            logger.error('Auto-analysis for report failed: %s', e)


def _generate_report_data(user_id: str) -> dict:
    """
    Generate a structured report from the user's latest analysis data.
    Report does NOT contain credentials, logs, or raw metrics.
    """
    _ensure_demo_recommendations(user_id)

    # Get user info
    user = query_db(
        "SELECT username, email FROM users WHERE id = ?",
        (user_id,), one=True
    )

    # Get AWS account info
    aws_account = query_db(
        "SELECT account_alias, region, last_synced FROM aws_accounts WHERE user_id = ?",
        (user_id,), one=True
    )

    # Get recommendations
    recommendations = query_db(
        """SELECT rule_id, resource_id, resource_type, recommendation_text,
                  severity, estimated_savings, created_at
           FROM recommendations
           WHERE user_id = ? AND status = 'ACTIVE'
           ORDER BY
               CASE severity WHEN 'HIGH' THEN 1 WHEN 'MEDIUM' THEN 2 WHEN 'LOW' THEN 3 END,
               estimated_savings DESC""",
        (user_id,)
    )

    rec_list = []
    total_savings = 0.0
    severity_counts = {'HIGH': 0, 'MEDIUM': 0, 'LOW': 0}

    for rec in recommendations:
        savings = rec['estimated_savings']
        total_savings += savings
        severity_counts[rec['severity']] = severity_counts.get(rec['severity'], 0) + 1
        rec_list.append({
            'resource': rec['resource_id'],
            'resource_type': rec['resource_type'],
            'issue': rec['rule_id'],
            'suggested_action': rec['recommendation_text'],
            'severity': rec['severity'],
            'estimated_savings': round(savings, 2)
        })

    report = {
        'report_metadata': {
            'title': 'RACE-Cloud Cost Optimization Report',
            'generated_at': datetime.utcnow().isoformat() + 'Z',
            'generated_by': 'RACE-Cloud Platform',
            'report_version': '1.0'
        },
        'account_summary': {
            'account_id': mask_string(aws_account['account_alias'], 4) if aws_account else 'N/A',
            'region': aws_account['region'] if aws_account else 'N/A',
            'analysis_date': aws_account['last_synced'] if aws_account else 'N/A',
            'prepared_for': user['username'] if user else 'N/A'
        },
        'optimization_recommendations': rec_list,
        'summary_statistics': {
            'total_issues_found': len(rec_list),
            'severity_breakdown': severity_counts,
            'total_estimated_monthly_savings': round(total_savings, 2)
        },
        'disclaimer': (
            'This report is generated by RACE-Cloud for advisory purposes only. '
            'No automatic changes have been or will be made to your AWS account. '
            'All recommendations should be reviewed by your team before implementation. '
            'Cost savings are estimates based on current pricing and usage patterns.'
        )
    }

    return report


