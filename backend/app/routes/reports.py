@reports_bp.route('/latest', methods=['GET'])
@jwt_required()
def get_latest_report():
    """Generate and return the latest report as JSON."""
    user_id = get_jwt_identity()

    report_data = _generate_report_data(user_id)

    # Store report
    aws_account = query_db(
        "SELECT id FROM aws_accounts WHERE user_id = ?",
        (user_id,), one=True
    )

    if aws_account:
        execute_db(
            "INSERT INTO reports (user_id, aws_account_id, report_data_json) VALUES (?, ?, ?)",
            (user_id, aws_account['id'], json.dumps(report_data))
        )

    return jsonify(report_data), 200


@reports_bp.route('/download', methods=['GET'])
@jwt_required()
def download_report():
    """Download the report as an HTML file."""
    user_id = get_jwt_identity()

    report_data = _generate_report_data(user_id)
    html_content = _generate_html_report(report_data)

    timestamp = datetime.utcnow().strftime('%Y%m%d_%H%M%S')
    filename = f'RACE-Cloud_Report_{timestamp}.html'

    return Response(
        html_content,
        mimetype='text/html',
        headers={
            'Content-Disposition': f'attachment; filename={filename}'
        }
    )


@reports_bp.route('/download-pdf', methods=['GET'])
@jwt_required()
def download_pdf_report():
    """
    Generate and download the report as a PDF file.
    PDF is built in-memory using ReportLab — no temp files.
    The PDF contains ONLY analysis output, never credentials.
    """
    user_id = get_jwt_identity()

    report_data = _generate_report_data(user_id)

    try:
        pdf_bytes = generate_pdf(report_data)
    except Exception as e:
        current_app.logger.error(f'PDF generation failed: {e}')
        return jsonify({'error': 'Failed to generate PDF report'}), 500

    timestamp = datetime.utcnow().strftime('%Y%m%d_%H%M%S')
    filename = f'racecloud_cost_report_{timestamp}.pdf'

    return Response(
        pdf_bytes,
        mimetype='application/pdf',
        headers={
            'Content-Disposition': f'attachment; filename={filename}'
        }
    )


@reports_bp.route('/email', methods=['POST'])
@jwt_required()
def email_report():
    """
    Generate a PDF report and email it to the logged-in user.
    1. Generates report data from latest analysis
    2. Builds PDF in-memory
    3. Sends email with PDF attachment via SMTP
    The email body includes a short summary of findings.
    """
    user_id = get_jwt_identity()

    # Get user info for email
    user = query_db(
        "SELECT username, email FROM users WHERE id = ?",
        (user_id,), one=True
    )
    if not user or not user['email']:
        return jsonify({'error': 'User email not found'}), 400

    report_data = _generate_report_data(user_id)

    # Check that there is analysis data
    recs = report_data.get('optimization_recommendations', [])
    summary = report_data.get('summary_statistics', {})
    if not recs and summary.get('total_issues_found', 0) == 0:
        # Still allow — report will show "no issues" which is valid
        pass

    # Generate PDF
    try:
        pdf_bytes = generate_pdf(report_data)
    except Exception as e:
        current_app.logger.error(f'PDF generation for email failed: {e}')
        return jsonify({'error': 'Failed to generate PDF report'}), 500

    # Send email
    result = send_report_email(
        recipient_email=user['email'],
        recipient_name=user['username'],
        pdf_bytes=pdf_bytes,
        report_summary=summary
    )

    status_code = 200 if result['success'] else 500
    return jsonify(result), status_code


@reports_bp.route('/history', methods=['GET'])
@jwt_required()
def get_report_history():
    """Get list of previously generated reports."""
    user_id = get_jwt_identity()

    reports = query_db(
        """SELECT id, created_at FROM reports
           WHERE user_id = ?
           ORDER BY created_at DESC
           LIMIT 20""",
        (user_id,)
    )

    return jsonify({
        'reports': [
            {'id': r['id'], 'created_at': r['created_at']}
            for r in reports
        ]
    }), 200
